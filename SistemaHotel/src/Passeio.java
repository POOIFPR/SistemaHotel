public class Passeio {
    String horario;
    String local;
    String tipoPasseio;
    String descricao;
    public Passeio() {
    }
    public Passeio(String horario, String local, String tipoPasseio, String descricao) {
        this.horario = horario;
        this.local = local;
        this.tipoPasseio = tipoPasseio;
        this.descricao = descricao;
    }
}
